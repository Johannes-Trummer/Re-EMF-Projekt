// Textzeile zeichnen
void draw_text(int x, int y, const char* str) {
    while (*str && x < WIDTH - 5) {
        char c = *str++;
        if (c < 32 || c > 127) c = '?';
        for (int i = 0; i < 5; i++) {
            uint8_t col = font5x7[c - 32][i];
            for (int j = 0; j < 7; j++) {
                draw_pixel(x, y + j, (col >> j) & 1);
            }
            x++;
        }
        x++; // Abstand zwischen Zeichen
    }
}

int main() {
    int fd = open(I2C_DEV, O_RDWR);
    if (fd < 0 || ioctl(fd, I2C_SLAVE, SSD1306_ADDR) < 0) {
        perror("I2C-Fehler");
        return 1;
    }

    memset(RAM, 0, sizeof(RAM));
    ssd1306_init(fd);

    draw_text(0, 0, "Hello, World!");
    draw_text(0, 10, "STM32MP1 Test");
    draw_text(0, 20, "Line 3 via I2C");

    ssd1306_draw(fd);
    close(fd);
    return 0;
}

