#include <linux/i2c-dev.h>
#include <sys/ioctl.h>

#define I2C_DEV "/dev/i2c-1"
#define SSD1306_ADDR 0x3C
#define WIDTH 128
#define HEIGHT 64
#define PAGES (HEIGHT / 8)

uint8_t RAM[WIDTH * PAGES];

// I2C-Kommando senden
void send_command(int fd, uint8_t cmd) {
    uint8_t data[2] = {0x00, cmd};
    write(fd, data, 2);
}

// I2C-Daten senden
void send_data(int fd, uint8_t data) {
    uint8_t buf[2] = {0x40, data};
    write(fd, buf, 2);
}

// SSD1306 initialisieren
void ssd1306_init(int fd) {
    send_command(fd, 0xAE); // Display off
    send_command(fd, 0x8D); send_command(fd, 0x14); // Charge Pump
    send_command(fd, 0x20); send_command(fd, 0x00); // Memory Addressing Mode: Horizontal
    send_command(fd, 0xA1); // Segment Re-map
    send_command(fd, 0xC8); // COM Output Scan Direction
    send_command(fd, 0xDA); send_command(fd, 0x12); // COM Pins
    send_command(fd, 0x81); send_command(fd, 0xFF); // Contrast
    send_command(fd, 0xD9); send_command(fd, 0xF1); // Precharge
    send_command(fd, 0xDB); send_command(fd, 0x40); // VCOMH
    send_command(fd, 0xA4); // Resume RAM
    send_command(fd, 0xA6); // Normal Display
    send_command(fd, 0x2E); // Deactivate scroll
    send_command(fd, 0xAF); // Display on
}

// RAM an Display senden
void ssd1306_draw(int fd) {
    for (int page = 0; page < PAGES; page++) {
        send_command(fd, 0xB0 + page);
        send_command(fd, 0x00);
        send_command(fd, 0x10);
        for (int col = 0; col < WIDTH; col++) {
            send_data(fd, RAM[page * WIDTH + col]);
        }
    }
}

// Pixel setzen
void draw_pixel(int x, int y, int color) {
    if (x < 0 || x >= WIDTH || y < 0 || y >= HEIGHT) return;
    int index = x + (y / 8) * WIDTH;
    uint8_t bit = 1 << (y % 8);
    if (color)
        RAM[index] |= bit;
    else
        RAM[index] &= ~bit;
}

